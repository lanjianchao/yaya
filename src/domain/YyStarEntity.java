package domain;

public class YyStarEntity {

	private int yyId;
	private String yyName;
	private String yyContent;
	private String yyPic;
	private String yyType;
	public int getYyId() {
		return yyId;
	}
	public void setYyId(int yyId) {
		this.yyId = yyId;
	}
	public String getYyName() {
		return yyName;
	}
	public void setYyName(String yyName) {
		this.yyName = yyName;
	}
	public String getYyContent() {
		return yyContent;
	}
	public void setYyContent(String yyContent) {
		this.yyContent = yyContent;
	}
	public String getYyPic() {
		return yyPic;
	}
	public void setYyPic(String yyPic) {
		this.yyPic = yyPic;
	}
	public String getYyType() {
		return yyType;
	}
	public void setYyType(String yyType) {
		this.yyType = yyType;
	}
	
	

}
